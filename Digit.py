number = input("Enter Number: ")
for digit in number:
    if digit == '1':
        print("One")
    elif digit == '2':
        print("Two")
    elif digit == '3':
        print("Three")
    elif digit == '4':
        print("Four")
    elif digit == '5':
        print("Five")
    elif digit == '6':
        print("Six")
    elif digit == '7':
        print("Seven")
    elif digit == '8':
        print("Eight")
    elif digit == '9':
        print("Nine")
    elif digit == '0':
        print("Zero")
